/**
 * যোগের পূর্ণাঙ্গ প্রেডিকশন প্রদান করে
 * @param {string} yogaName - যোগের নাম
 * @returns {string} পূর্ণাঙ্গ প্রেডিকশন
 */
function getYogaFullPrediction(yogaName) {
    if (YOGA_FULL_PREDICTIONS[yogaName]) {
        return YOGA_FULL_PREDICTIONS[yogaName].full;
    }
    return `${yogaName} আপনার কুষ্ঠিতে বর্তমান রয়েছে। এটি একটি বিশেষ যোগ, যা আপনার জীবনে গুরুত্বপূর্ণ প্রভাব ফেলবে।`;
}

/**
 * সমস্ত যোগের প্রেডিকশন একত্রে প্রদান করে
 * @param {array} yogas - detectAllYogas থেকে প্রাপ্ত যোগের তালিকা
 * @returns {string} সমস্ত যোগের প্রেডিকশন
 */
function getAllYogaPredictions(yogas) {
    if (!yogas || yogas.length === 0) {
        return "আপনার কুষ্ঠিতে কোনো বিশেষ যোগ পাওয়া যায়নি।";
    }
    
    let output = "✨ কুষ্ঠিগত বিশেষ যোগের ফলাফল\n";
    output += "─".repeat(40) + "\n\n";
    
    yogas.forEach((yoga, index) => {
        output += `🕉️ ${yoga.name}\n`;
        output += getYogaFullPrediction(yoga.name) + "\n\n";
        if (index < yogas.length - 1) {
            output += "─".repeat(30) + "\n\n";
        }
    });
    
    return output;
}

