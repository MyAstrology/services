/**
 * অন্তর্দশার প্রেডিকশন প্রদান করে
 * @param {string} mdLord - মহাদশার অধিপতি
 * @param {string} adLord - অন্তর্দশার অধিপতি
 * @returns {string} পূর্ণাঙ্গ প্রেডিকশন
 */
function getAntarDashaPrediction(mdLord, adLord) {
    const key = `${mdLord}_${adLord}`;
    if (ANTARDASHA_PREDICTIONS[key]) {
        return ANTARDASHA_PREDICTIONS[key].full;
    }
    return `${mdLord} মহাদশার ${adLord} অন্তর্দশা চলছে। এই সময়ে আপনার জীবনে গুরুত্বপূর্ণ পরিবর্তন আসবে।`;
}

/**
 * বর্তমান দশার পূর্ণাঙ্গ প্রেডিকশন প্রদান করে
 * @param {object} dashaInfo - getFullDashaInfo থেকে প্রাপ্ত তথ্য
 * @returns {string} পূর্ণাঙ্গ প্রেডিকশন
 */
function getCurrentDashaPrediction(dashaInfo) {
    if (!dashaInfo || !dashaInfo.currentMD || !dashaInfo.currentAD) {
        return "দশার তথ্য পাওয়া যায়নি।";
    }
    
    const mdLord = dashaInfo.currentMD.lord;
    const adLord = dashaInfo.currentAD.lord;
    const pdLord = dashaInfo.currentPD ? dashaInfo.currentPD.lord : null;
    
    let output = "";
    
    // মহাদশার সাধারণ তথ্য
    output += `বর্তমানে আপনার ${mdLord} মহাদশা চলছে (মোট ${dashaInfo.currentMD.period} বছরের মধ্যে ${dashaInfo.currentMD.elapsed.toFixed(2)} বছর অতিবাহিত)।\n`;
    output += `অন্তর্দশা: ${adLord} (${dashaInfo.currentAD.total.toFixed(2)} বছরের মধ্যে ${dashaInfo.currentAD.elapsed.toFixed(2)} বছর অতিবাহিত)।\n`;
    
    if (pdLord) {
        output += `প্রত্যংতর দশা: ${pdLord}।\n`;
    }
    
    output += "\n" + getAntarDashaPrediction(mdLord, adLord);
    
    return output;
}

