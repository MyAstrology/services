    // ==================== HOUSE CALCULATION ====================
    getHouse(planetSign, lagnaSign) {
        const si = this.signs.indexOf(planetSign);
        const li = this.signs.indexOf(lagnaSign);
        if (si === -1 || li === -1) return 1;
        return ((si - li + 12) % 12) + 1;
    }

    // ==================== SIGN TYPE DETECTION ====================
    getSignType(planet, sign) {
        if (this.exalted[planet] === sign) return "exalted";
        if (this.debilitated[planet] === sign) return "debilitated";
        if (this.ownSigns[planet]?.includes(sign)) return "own";
        if (this.friendSigns[planet]?.includes(sign)) return "friend";
        if (this.enemySigns[planet]?.includes(sign)) return "enemy";
        return "neutral";
    }

    // ==================== CORE PREDICT METHOD ====================
    predict(planet, house, sign) {
        const basePred = this.base[planet]?.[house];
        if (!basePred) return { error: "তথ্য পাওয়া যায়নি" };

        // মডিফায়ার বের করা
        const signType = this.getSignType(planet, sign);
        const modifier = this.modifiers[signType];
        let modifierText = "";
        
        if (modifier && modifier.full) {
            modifierText = "\n\n" + modifier.prefix + "\n" + 
                modifier.full
                    .replace(/\{planet\}/g, planet)
                    .replace(/\{sign\}/g, sign);
        }

        return {
            planet,
            dashaYears: this.dashaYears[planet],
            house,
            houseName: this.houseNames[house],
            sign,
            signType,
            type: basePred.type,
            summary: basePred.summary,
            full: basePred.full + modifierText,
            baseOnly: basePred.full,
            modifierOnly: modifierText
        };
    }

