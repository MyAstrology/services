/**
 * Lahiri অয়নাংশ গণনা
 * @param {number} jd - জুলিয়ান দিন
 * @returns {number} - অয়নাংশ (ডিগ্রিতে)
 */
function lahiriAY(jd) {
  const T = (jd - 2451545) / 36525;
  return 23.853056 + (50.2564 + 0.0222 * T) / 3600 * T * 100;
}

module.exports = { lahiriAY };
