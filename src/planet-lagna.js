function getNakshatraPrediction(nakshatraName, pada = 1) {
    const nakshatra = NAKSHATRA_PREDICTIONS[nakshatraName];
    if (!nakshatra) return null;
    
    const padaKey = `pada${pada}`;
    const padaData = nakshatra[padaKey] || { nature: "তথ্য নেই", profession: "তথ্য নেই" };
    
    return {
        ...nakshatra,
        currentPada: padaData
    };
}

