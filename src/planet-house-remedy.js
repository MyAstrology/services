function getTriShaktiRemedy(planet) {
    return TRI_SHAKTI_REMEDIES[planet] || null;
}

function generateFullRemedyText(weakPlanets) {
    if (!weakPlanets || weakPlanets.length === 0) {
        return "আপনার কুষ্ঠিতে সমস্ত গ্রহ শক্তিশালী অবস্থানে রয়েছে। তবে নিয়মিত দান-সেবা ও শিবের আরাধনা জীবনকে আরও সুন্দর করবে।";
    }
    
    let output = "🙏 ত্রিশক্তি তত্ত্ব, লাল কিতাব ও মানবসেবার সমন্বয়ে গ্রহ শান্তির প্রতিকার\n";
    output += "═".repeat(55) + "\n\n";
    output += "গ্রহের অশুভ প্রভাব কাটাতে শুধু পূজা নয়, সমাজের অসহায় মানুষদের সেবা করাও সমান জরুরি। নিচে প্রতিটি গ্রহের জন্য আলাদাভাবে প্রতিকার দেওয়া হলো।\n\n";
    
    weakPlanets.forEach((wp, index) => {
        const remedy = TRI_SHAKTI_REMEDIES[wp.planet];
        if (!remedy) return;
        
        output += `🟊 ${wp.planet} (${wp.severity} দুর্বলতা)\n`;
        output += "─".repeat(40) + "\n\n";
        output += remedy.problem + "\n\n";
        output += remedy.philosophy + "\n\n";
        output += "📿 লাল কিতাব টোটকা:\n" + remedy.lalKitabTotka + "\n\n";
        output += "🔔 পূজা অর্চনা:\n" + remedy.puja + "\n\n";
        output += "💚 মানবসেবা (সবচেয়ে গুরুত্বপূর্ণ):\n" + remedy.seva + "\n\n";
        output += "🎁 দান:\n" + remedy.donation + "\n\n";
        output += "⚠️ সতর্কতা:\n" + remedy.caution + "\n";
        
        if (index < weakPlanets.length - 1) {
            output += "\n" + "═".repeat(55) + "\n\n";
        }
    });
    
    output += "\n" + "═".repeat(55) + "\n";
    output += "🙏 ওঁ নমঃ শিবায়। মনে রাখবেন—পৃথিবীর সবচেয়ে বড় পূজা হলো মানুষের সেবা। আপনার জীবন মঙ্গলময় হোক।\n";
    
    return output;
}

