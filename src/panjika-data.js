// getPanjikaEvents() — একটি তারিখের সব ইভেন্ট একত্রিত করে
// TITHI_FESTS ও SANKRANTI_FESTS এখানে পরিচালিত হয় না —
// সেগুলো panjika.html-এর getUnifiedFests() করে।
// এই function শুধু PANJIKA_DATA + FIXED_ANNUAL_FESTS পরিচালনা করে।
// ════════════════════════════════════════════════════════════════════
function getPanjikaEvents(dateStr, mmdd) {
  const result = {
    festivals: [],   // PANJIKA_DATA.festivals থেকে
    births:    [],   // birthAnniversaries
    deaths:    [],   // deathAnniversaries
    modern:    [],   // modernDays + FIXED_ANNUAL_FESTS
    malmas:    null, // মলমাস
    sankranti: []    // panjika.html থেকে inject হবে
  };

  // ১. PANJIKA_DATA.festivals (এখন খালি — ভবিষ্যতে বিশেষ দিন যোগ করা যাবে)
  PANJIKA_DATA.festivals.forEach(f => {
    if (f.date === dateStr) result.festivals.push(f);
  });

  // ২. মহাপুরুষের জন্মদিন (MM-DD মিলিয়ে)
  PANJIKA_DATA.birthAnniversaries.forEach(b => {
    if (b.mmdd === mmdd) result.births.push(b);
  });

  // ৩. মহাপুরুষের তিরোধান দিবস (MM-DD মিলিয়ে)
  PANJIKA_DATA.deathAnniversaries.forEach(d => {
    if (d.mmdd === mmdd) result.deaths.push(d);
  });

  // ৪. আধুনিক দিবস — নির্দিষ্ট তারিখ
  PANJIKA_DATA.modernDays.forEach(m => {
    if (m.date === dateStr) result.modern.push(m);
  });

  // ৫. FIXED_ANNUAL_FESTS — MM-DD ভিত্তিক বার্ষিক উৎসব
  FIXED_ANNUAL_FESTS.forEach(f => {
    // duplicate এড়াতে name check করি
    const alreadyExists = result.modern.some(m => m.name === f.name)
                       || result.festivals.some(x => x.name === f.name);
    if (f.mmdd === mmdd && !alreadyExists) {
      result.modern.push({ date: dateStr, name: f.name, icon: f.icon, message: '' });
    }
  });

  // ৬. মলমাস
  const dt = new Date(dateStr + 'T00:00:00');
  PANJIKA_DATA.malmas.forEach(ml => {
    const s = new Date(ml.startDate + 'T00:00:00');
    const e = new Date(ml.endDate + 'T23:59:59');
    if (dt >= s && dt <= e) result.malmas = ml;
  });

  return result;
}
