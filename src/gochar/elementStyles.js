/**
 * ডিটারমিনিস্টিক এলিমেন্ট টেক্সট বাছাই
 * একই তারিখ ও রাশির জন্য সবসময় একই টেক্সট ফেরত দেয়
 * @param {Array} arr - টেক্সটের অ্যারে
 * @param {string} seed - সীড (তারিখ + রাশি + বিভাগ)
 * @returns {string}
 */
function getDeterministicElement(arr, seed) {
  if (!arr || arr.length === 0) return '';
  
  // উন্নত হ্যাশ ফাংশন (djb2)
  let hash = 5381;
  for (let i = 0; i < seed.length; i++) {
    hash = ((hash << 5) + hash) + seed.charCodeAt(i);
    hash = hash >>> 0; // 32-bit unsigned integer
  }
  return arr[hash % arr.length];
}

