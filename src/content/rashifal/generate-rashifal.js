function hasAspect(fromRashi, toRashi, aspectHouses) {
  const diff = ((toRashi - fromRashi + 12) % 12) + 1;
  return aspectHouses.includes(diff);
}

function getAspectBonus(planetRashis, targetRashi) {
  let bonus = 0;
  if (planetRashis.jupiter !== undefined &&
      hasAspect(planetRashis.jupiter, targetRashi, [5, 7, 9]))  bonus += 1;
  if (planetRashis.saturn  !== undefined &&
      hasAspect(planetRashis.saturn,  targetRashi, [3, 7, 10])) bonus -= 0.5;
  if (planetRashis.mars    !== undefined &&
      hasAspect(planetRashis.mars,    targetRashi, [4, 7, 8]))  bonus -= 0.3;
  return bonus;
}

function sunTimes(y,m,d){
  const jd0=JD(y,m,d), n=jd0-2451545+0.5;
  const L=((280.46646+0.9856474*n)%360+360)%360;
  const g=((357.52911+0.9856003*n)%360+360)*Math.PI/180;
  const lam=L+1.914602*Math.sin(g)+0.019993*Math.sin(2*g);
  const eps=23.439*Math.PI/180, lamR=lam*Math.PI/180;
  const sinDec=Math.sin(eps)*Math.sin(lamR);
  const dec=Math.asin(sinDec);
  const e2=0.016708634, y2=Math.tan(eps/2)**2, lR=L*Math.PI/180;
  const EoT=4*(y2*Math.sin(2*lR)-2*e2*Math.sin(g)+4*e2*y2*Math.sin(g)*Math.cos(2*lR)
    -0.5*y2*y2*Math.sin(4*lR)-1.25*e2*e2*Math.sin(2*g))*180/Math.PI;
  const latR=LAT*Math.PI/180;
  const cosH=(Math.sin(-0.8333*Math.PI/180)-Math.sin(latR)*sinDec)/(Math.cos(latR)*Math.cos(dec));
  if(Math.abs(cosH)>1) return {rise:6,set:18};
  const H=Math.acos(Math.min(1,Math.max(-1,cosH)))*180/Math.PI;
  const noon=(720-4*LNG-EoT)/60+TZ;
  return {rise:noon-H/15, set:noon+H/15};
}

