function getRashiIdx(longitude, jd) {
  const ay = lahiriAY(jd);
  return Math.floor(((longitude - ay) % 360 + 360) % 360 / 30);
}

function getTithiName(jd) {
  const idx = Math.floor(((moonL(jd) - sunL(jd) + 360) % 360) / 12) % 30;
  const paksha = idx < 15 ? 'শুক্লপক্ষ' : 'কৃষ্ণপক্ষ';
  return paksha + ' ' + TNAMES[idx];
}

function getNakshatraName(jd) {
  const ay = lahiriAY(jd);
  const idx = Math.floor(((moonL(jd) - ay + 360) % 360) / (360/27)) % 27;
  return NAKS[idx];
}

function getYogaName(jd) {
  const ay = lahiriAY(jd);
  const idx = Math.floor(((sunL(jd)-ay+moonL(jd)-ay+720)%360)/(360/27))%27;
  return YOGAS[idx];
}

function getKaranName(jd) {
  const n = Math.floor(((moonL(jd)-sunL(jd)+360)%360)/6)%60;
  const k = ((n%60)+60)%60;
  if(k===0) return KARANS[10];
  if(k===57) return KARANS[7];
  if(k===58) return KARANS[8];
  if(k===59) return KARANS[9];
  return KARANS[(k-1)%7];
}

function getBnDate(date) {
  const t = date.getTime();
  let idx = -1;
  for(let i = BMS.length-1; i >= 0; i--) {
    if(t >= new Date(BMS[i].s+'T00:00:00').getTime()) { idx=i; break; }
  }
  if(idx < 0) return null;
  const e = BMS[idx];
  const s = new Date(e.s+'T00:00:00');
  const d = Math.floor((t - s.getTime()) / 86400000) + 1;
  return { y: e.y, m: e.m, d, name: BN_MONTHS[e.m] };
}

function fmtTime(h) {
  const hh = Math.floor(h), mm = Math.round((h - hh) * 60);
  const hh12 = hh % 12 === 0 ? 12 : hh % 12;
  const ampm = hh < 12 ? 'সকাল' : hh < 15 ? 'দুপুর' : hh < 17 ? 'বিকেল' : hh < 20 ? 'সন্ধ্যা' : 'রাত';
  const mmStr = String(mm).padStart(2,'0').replace(/[0-9]/g,d=>BD[+d]);
  return `${ampm} ${toBn(hh12)}:${mmStr}`;
}

function getRahuKal(rise, set, weekday) {
  const RS = [4,2,6,5,6,4,3];
  const len = (set - rise) / 8;
  const s = rise + len * (RS[weekday] - 1);
  const e = s + len;
  return fmtTime(s) + ' – ' + fmtTime(e);
}


