function generateRashifalData(date){
  const y=date.getFullYear(),m=date.getMonth()+1,d=date.getDate();
  const jd=JD(y,m,d)+0.5,ay=lahiriAY(jd);
  const sid=lng=>Math.floor(((lng-ay)%360+360)%360/30);

  const moonRashi   =sid(moonL(jd));
  const sunRashi    =sid(sunL(jd));
  const saturnRashi =sid(saturnL(jd));
  const jupiterRashi=sid(jupiterL(jd));
  const marsRashi   =sid(marsL(jd));
  const mercuryRashi=sid(mercuryL(jd));
  const venusRashi  =sid(venusL(jd));
  const rahuRashi   =sid(rahuL(jd));
  const ketuRashi   =(rahuRashi+6)%12;
  const retro       =getPlanetRetrogrades(jd);

  const allPR={jupiter:jupiterRashi,saturn:saturnRashi,mars:marsRashi,
               sun:sunRashi,mercury:mercuryRashi,venus:venusRashi,
               rahu:rahuRashi,ketu:ketuRashi};

  const GOOD=new Set([3,6,9,10,11]);
  const BAD =new Set([1,2,4,5,7,8,12]);
  const pSt =h=>GOOD.has(h)?'good':BAD.has(h)?'bad':'neutral';
  const pLb =h=>GOOD.has(h)?'শুভ':BAD.has(h)?'অশুভ':'মধ্যম';
  const clamp=(v,lo,hi)=>Math.max(lo,Math.min(hi,v));
  const gData=buildEnhancedGochar();
  const dateStr=date.toISOString().slice(0,10);
  const result=[];

  for(let ri=0;ri<12;ri++){
    const house=((moonRashi-ri+12)%12)+1;
    // ★ CRITICAL FIX: deep clone — Object Mutation Bug সম্পূর্ণ দূর
    const g=JSON.parse(JSON.stringify(gData[house]));

    // element style variation
    const el=RASHI_EL[ri];
    const es=ELEMENT_STYLES[el]||ELEMENT_STYLES['পৃথিবী'];
    g.love.short   =getDet(es.love,   `${dateStr}-${ri}-love`);
    g.work.short   =getDet(es.work,   `${dateStr}-${ri}-work`);
    g.health.short =getDet(es.health, `${dateStr}-${ri}-health`);
    g.finance.short=getDet(es.finance,`${dateStr}-${ri}-finance`);

    // spiritual variation
    const sp=SPIRITUAL_POOLS[house]||SPIRITUAL_POOLS[1];
    g.spiritual=getDet(sp,`${dateStr}-${ri}-spirit`);

    // score adjustment
    const ab=getAspectBonus(allPR,ri);
    const sc={
      love:   clamp(Math.round(g.score.love   +ab),1,5),
      work:   clamp(Math.round(g.score.work   +ab),1,5),
      health: clamp(Math.round(g.score.health +Math.floor(ab*0.5)),1,5),
      finance:clamp(Math.round(g.score.finance+ab),1,5),
    };

    const sunH =((sunRashi    -ri+12)%12)+1;
    const satH =((saturnRashi -ri+12)%12)+1;
    const jupH =((jupiterRashi-ri+12)%12)+1;
    const marH =((marsRashi   -ri+12)%12)+1;
    const merH =((mercuryRashi-ri+12)%12)+1;
    const venH =((venusRashi  -ri+12)%12)+1;
    const rahH =((rahuRashi   -ri+12)%12)+1;
    const ketH =((ketuRashi   -ri+12)%12)+1;

    const mkP=(ico,name,tbl,h,isR)=>({ico,name,...(tbl[h]||{tag:'-',col:'#999',txt:''}),
      house:h,status:pSt(h),statusLabel:pLb(h),houseLabel:`${toBn(h)}ম ভাবে`,retro:isR||false});

    const planets=[
      mkP('☀️','সূর্য',    SURYA_G,sunH, false),
      mkP('♃', 'বৃহস্পতি',GURU_G, jupH, retro.jupiter),
      mkP('🪐','শনি',      SHANI_G,satH, retro.saturn),
      mkP('♂', 'মঙ্গল',   MARS_G, marH, retro.mars),
      mkP('☿', 'বুধ',      MERC_G, merH, retro.mercury),
      mkP('♀', 'শুক্র',    VENUS_G,venH, retro.venus),
      mkP('☊', 'রাহু',     RAHU_G, rahH, false),
      mkP('☋', 'কেতু',     KETU_G, ketH, false),
    ];

    const planetFooter=`☀️ ${RASHI_NAMES[sunRashi]} · ♃ ${RASHI_NAMES[jupiterRashi]} · 🪐 ${RASHI_NAMES[saturnRashi]} · ♂ ${RASHI_NAMES[marsRashi]} · ☿ ${RASHI_NAMES[mercuryRashi]} · ♀ ${RASHI_NAMES[venusRashi]} · ☊ ${RASHI_NAMES[rahuRashi]} · ☋ ${RASHI_NAMES[ketuRashi]}`;
    const rashiInfo=`${RASHI_ENG[ri]} | অধিপতি: ${RASHI_LORD[ri]} | ${RASHI_EL[ri]} | ${RASHI_NAT[ri]}`;
    const sadeSati=getSadeSatiInfo(satH);
    const moonAspects=getMoonAspects(moonRashi,allPR);
    const mercuryRetroNote=retro.mercury?'বুধ বক্রী চলছে — চুক্তি, যোগাযোগ, ভ্রমণে বিশেষ সতর্কতা। পুরনো বিষয় পর্যালোচনার উপযুক্ত সময়।':null;

    result.push({
      rashi:RASHI_NAMES[ri],rashiInfo,house,
      tag:g.tag,tagCol:g.tagCol,gocharLabel:g.gocharLabel,summary:g.summary,
      score:sc,love:g.love,work:g.work,health:g.health,finance:g.finance,
      spiritual:g.spiritual,
      lucky:{nums:g.lucky.nums,colors:g.lucky.colors,goodTime:g.lucky.goodTime,
             badTime:g.lucky.badTime,dir:LUCKY_DIRS[ri]},
      gem:RASHI_GEM[ri],caution:g.caution,mantra:MANTRAS[ri],
      planets,planetFooter,sadeSati,moonAspects,mercuryRetroNote,retro,
    });
  }
  return{moonRashi,sunRashi,saturnRashi,jupiterRashi,marsRashi,
         mercuryRashi,venusRashi,rahuRashi,ketuRashi,retro,data:result};
}

