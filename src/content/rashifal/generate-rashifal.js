// ★ buildHtml — Google News GMT Meta Tags সংশোধিত
// ════════════════════════════════════
function buildHtml(date){
  const templateContent=fs.readFileSync(TEMPLATE,'utf8');
  const y=date.getFullYear(),m=date.getMonth()+1,d=date.getDate();
  const jd=JD(y,m,d)+0.5;
  const rf=generateRashifalData(date);
  const panchang={tithi:getTithiName(jd),nakshatra:getNakshatraName(jd),yoga:getYogaName(jd),karan:getKaranName(jd)};
  const st=sunTimes(y,m,d);
  const rahuKal=getRahuKal(st.rise,st.set,date.getDay());
  const iso=date.toISOString().slice(0,10);
  const bnDate=formatBnDate(date);
  const bnDateFull=formatBnDateFull(date);
  const moonN=RASHI_NAMES[rf.moonRashi];
  const sunN =RASHI_NAMES[rf.sunRashi];

  // ★ Google News-এর জন্য সঠিক GMT তারিখ
  const gmtPub=new Date(`${iso}T05:00:00+05:30`).toUTCString();
  // ★ news_keywords — Google News indexing-এর জন্য
  const newsKw=`দৈনিক রাশিফল,${bnDate} রাশিফল,বাংলা রাশিফল,চন্দ্র ${moonN} রাশি,সূর্য ${sunN} রাশি,ড. প্রদ্যুৎ আচার্য`;
  const clientScript=buildClientScript(rf);

  let html=templateContent
    .replace(/\{\{BN_DATE\}\}/g,        bnDate)
    .replace(/\{\{BN_DATE_FULL\}\}/g,   bnDateFull)
    .replace(/\{\{URL_DATE\}\}/g,        iso)
    .replace(/\{\{ISO_DATE\}\}/g,        iso)
    .replace(/\{\{GMT_PUB_DATE\}\}/g,    gmtPub)
    .replace(/\{\{NEWS_KEYWORDS\}\}/g,   newsKw)
    .replace(/\{\{MOON_RASHI\}\}/g,     moonN)
    .replace(/\{\{SUN_RASHI\}\}/g,      sunN)
    .replace(/\{\{TITHI\}\}/g,           panchang.tithi)
    .replace(/\{\{NAKSHATRA\}\}/g,       panchang.nakshatra)
    .replace(/\{\{YOGA\}\}/g,            panchang.yoga)
    .replace(/\{\{KARAN\}\}/g,           panchang.karan)
    .replace('{{RAHU_KAL}}',             rahuKal)
    .replace('{{SCHEMA_JSON}}',          buildSchema(date,rf))
    .replace('{{RASHI_CARDS}}',          buildRashiCards(rf))
    .replace('{{DEFAULT_RASHI_DETAIL}}', buildDefaultRashiDetail(rf))
    .replace('{{ARCHIVE_LINKS}}',        buildArchiveLinks(date,rf))
    .replace('{{RASHIFAL_DATA_JSON}}',   JSON.stringify(rf.data))
    .replace('{{CLIENT_SCRIPT}}',        clientScript)
    .replace('{{FAQ_SCHEMA_JSON}}',      buildFaqSchema(date,rf));

  return html;
}

function generateIndex(){
  const files=fs.readdirSync(OUTPUT_DIR).filter(f=>/^\d{4}-\d{2}-\d{2}\.html$/.test(f)).sort().reverse().slice(0,30);
  const links=files.map(f=>{const iso=f.replace('.html',''),dd=new Date(iso+'T00:00:00'),bn=getBnDate(dd);const label=bn?`${toBn(bn.d)} ${bn.name} ${toBn(bn.y)}`:iso;return `<li><a href="/rashifal/${f}">${label} — ${iso}</a></li>`;}).join('\n');
  const indexHtml=`<!DOCTYPE html><html lang="bn-IN" translate="no"><head><meta charset="UTF-8"><title>দৈনিক রাশিফল সংগ্রহ | MyAstrology</title><meta name="description" content="ড. প্রদ্যুৎ আচার্যের বৈদিক জ্যোতিষভিত্তিক দৈনিক রাশিফল সংগ্রহ।"><link rel="canonical" href="https://www.myastrology.in/rashifal/"></head><body><h1>দৈনিক রাশিফল সংগ্রহ</h1><ul>${links}</ul></body></html>`;
  fs.writeFileSync(path.join(OUTPUT_DIR,'index.html'),indexHtml,'utf8');
  console.log('✅ rashifal/index.html updated');
}

